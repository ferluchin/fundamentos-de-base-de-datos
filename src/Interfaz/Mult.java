    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        // TODO add your handling code here:
        try {
            String sInsert = "INSERT INTO Modelo VALUES(?,?,?,?,?)";
            PreparedStatement datos = conn.prepareCall(sInsert);

            datos.setString(1, txt_Anio.getText());
            datos.setString(2, txt_Particular.getText());
            datos.setString(3, txt_Alquiler.getText());
            datos.setString(4, txt_Estado.getText());
            datos.setString(5, txt_Municipal.getText());

            int n = datos.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(null, "Datos Guardados");
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, "Error" + e.getMessage());
        }
//        Llenar();
        Limpiar();

    }//GEN-LAST:event_GuardarActionPerformed

