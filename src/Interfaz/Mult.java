    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getButton() == 1) {
            int fila = jTable1.getSelectedRow();
            try {
                Habilitar();
                String cons = "SELECT * FROM Modelo WHERE Anio='" + jTable1.getValueAt(fila, 0) + "'";
                sent = conn.createStatement();
                ResultSet consulta = sent.executeQuery(cons);
                consulta.next();
                txt_Anio.setText(consulta.getString("Anio"));
                txt_Particular.setText(consulta.getString("Particular"));
                txt_Alquiler.setText(consulta.getString("Alquiler"));
                txt_Estado.setText(consulta.getString("Estado"));
                txt_Municipal.setText(consulta.getString("Municipal"));
            } catch (SQLException e) {
                System.out.print(e.getMessage());
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        // TODO add your handling code here:
        int fila = jTable1.getSelectedRow();
        String dao = (String) jTable1.getValueAt(fila, 0);
        try {
            String sql = "UPDATE Modelo SET "
                    + "Anio='" + txt_Anio.getText() + "',"
                    + "Particular='" + txt_Particular.getText() + "',"
                    + "Alquiler='" + txt_Alquiler.getText() + "',"
                    + "Estado='" + txt_Estado.getText() + "',"
                    + "Municipal='" + txt_Municipal.getText() + "'\n"
                    + "WHERE Anio='" + dao + "';";
            PreparedStatement ps = conn.prepareStatement(sql);
            int n = ps.executeUpdate();
            if (n > 0) {
                Limpiar();
                JOptionPane.showMessageDialog(null, "Datos Modificados");
            }
        } catch (Exception e) {
            System.out.print(e.getMessage());
            JOptionPane.showMessageDialog(null, "Error" + e.getMessage());
        }
    }//GEN-LAST:event_ModificarActionPerformed

